package poo;

public class Conta {
	int id;
	String tipo;
	Double saldo;
	public int getId() {
	return id;
	}
	public void setId(int id) {
	this.id = id;
	}
	public String getTipo() {
	return tipo;
	}
	public void setTipo(String tipo) {
	this.tipo = tipo;
	}
	public Double getSaldo() {
	return saldo;
	}
	public void setSaldo(Double saldo) {
	this.saldo = saldo;
	}

}
