package poo;

public class Carro {
	String Cor;
	int Porta;
	int Rodas;
	String Tipo;
	public String getCor() {
	return Cor;
	}
	public void setCor(String cor) {
	this.Cor = cor;
	}
	public int getPorta() {
	return Porta;
	}
	public void setPorta(int porta) {
	this.Porta = porta;
	}
	public int setRodas() {
	return Rodas;
	}
	public void setRodas(int Rodas) {
	this.Rodas = Rodas;
	}
	public String getTipo() {
	return Tipo;
	}
	public void setTipo(String Tipo) {
	this.Tipo = Tipo;
	}
	
}
