package poo;

public class Fornecedor {
	int id;
	String nomefant;
	String razaosocial;
	String cnpj;
	String endereco;
	String fone;
	String email;
	String inscriEstadual;
	public int getId() {
	return id;
	}
	public void setId(int id) {
	this.id = id;
	}
	public String getNomefant() {
	return nomefant;
	}
	public void setNomefant(String nomefant) {
	this.nomefant = nomefant;
	}
	public String getRazaosocial() {
	return razaosocial;
	}
	public void setRazaosocial(String razaosocial) {
	this.razaosocial = razaosocial;
	}
	public String getCnpj() {
	return cnpj;
	}
	public void setCnpj(String cnpj) {
	this.cnpj = cnpj;
	}
	public String getEndereco() {
	return endereco;
	}
	public void setEndereco(String endereco) {
	this.endereco = endereco;
	}
	public String getFone() {
	return fone;
	}
	public void setFone(String fone) {
	this.fone = fone;
	}
	public String getEmail() {
	return email;
	}
	public void setEmail(String email) {
	this.email = email;
	}
	public String getInscriEstadual() {
	return inscriEstadual;
	}
	public void setInscriEstadual(String inscriEstadual) {
	this.inscriEstadual = inscriEstadual;
	}

}
